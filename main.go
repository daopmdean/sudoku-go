package main

import (
	"fmt"
	"sudoku/sudoku"
)

func main() {
	in := [][]int{
		{6, 0, 0, 8, 0, 0, 3, 0, 0},
		{5, 0, 0, 0, 2, 0, 0, 0, 0},
		{1, 0, 0, 4, 0, 3, 0, 5, 6},
		{0, 6, 1, 0, 0, 0, 5, 9, 8},
		{9, 0, 4, 6, 0, 5, 0, 0, 2},
		{8, 3, 5, 0, 0, 9, 7, 0, 0},
		{3, 0, 9, 5, 0, 8, 4, 0, 0},
		{7, 8, 0, 0, 4, 2, 9, 1, 0},
		{0, 0, 2, 0, 0, 0, 0, 8, 0},
	}

	_ = [][]int{
		{6, 2, 7, 8, 5, 1, 3, 4, 9},
		{5, 0, 0, 0, 2, 0, 0, 0, 0},
		{1, 0, 0, 4, 0, 3, 0, 5, 6},
		{0, 6, 1, 0, 0, 0, 5, 9, 8},
		{9, 0, 4, 6, 0, 5, 0, 0, 2},
		{8, 3, 5, 0, 0, 9, 7, 0, 0},
		{3, 0, 9, 5, 0, 8, 4, 0, 0},
		{7, 8, 0, 0, 4, 2, 9, 1, 0},
		{0, 0, 2, 0, 0, 0, 0, 8, 0},
	}

	o, err := sudoku.Solve(in)
	if err != nil {
		fmt.Println(err)
		return
	}

	print(o)
	fmt.Println("========================")
	fmt.Println()
	print(in)
}

func print(maze [][]int) {
	for i := range maze {
		for j := range maze[i] {
			fmt.Print(maze[i][j], " ")
			if j == 2 || j == 5 {
				fmt.Print("| ")
			}
		}
		if i == 2 || i == 5 {
			fmt.Print("\n-+-+--+--+-+--+--+-+--\n")
		} else {
			fmt.Print("\n")
		}
	}
}
